package com.johnson.securitymonitoringsystem.bean;

public class MonitorData {
    private int red;
    private double temp;
    private int fire;

    public int getRed() {
        return red;
    }

    public void setRed(int red) {
        this.red = red;
    }

    public double getTemp() {
        return temp;
    }

    public void setTemp(double temp) {
        this.temp = temp;
    }

    public int getFire() {
        return fire;
    }

    public void setFire(int fire) {
        this.fire = fire;
    }
}
